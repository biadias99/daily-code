/* 
- Name: 2235. Add Two Integers
- Description: Given two integers num1 and num2, return the sum of the two integers.
- My line of thought: Basic addition of two integers is straightforward. I can simply return the sum of the two numbers.
- Time complexity: O(1)
- Space complexity: O(1)
*/

function sum (num1, num2) {
    return num1 + num2;
}